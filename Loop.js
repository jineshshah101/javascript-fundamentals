// Loops are use to let things run continuously based on the criteria given

// While loop

num1 = 0;
while(num1 < 50)
{
    num1 += 1;
    window.alert(num1);
}

// For loop
// var and let are pretty much the same thing

for(let num2 = 0; num2<=50; num2++)
{
    window.alert(num2);
}