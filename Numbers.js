// Can have numbers in javascript

var num1 = 20;
var num2 = 15;
var num3 = 10;
var num4 = 5;
var num5 = 20;
var num6 = 30;
var num7 = 40;

// increment number by 1

var incrnum = num1++
window.alert(incrnum);

// decrement number by 1

var decrnum = num2--
window.alert(decrnum);

// divide numbers

var divnum = num3/2;
window.alert(divnum);

// multiply numbers

var mulnum = num4 * 5;
window.alert(mulnum);

// getting remainder values of numbers

var remanum = num5 % 3;
window.alert(remanum);

// increment by any number you wish

var incanynum = num6 += 10;
